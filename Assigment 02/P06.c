// WAP to Convet Character into ASCII Code.

#include<stdio.h>

int main() {
    
    char ch;
    
    printf("Enter any Character : ");
    scanf("%c",&ch);
    
    printf("Your Character is \"%c\" and ASCII value is : %d",ch,ch);
   
    printf("\n");
    return 0;
}


